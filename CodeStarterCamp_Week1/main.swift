func drawIcecreamBody() {
    let lines = 8
    for _ in 1...lines {
        print("***********")
    }
}

func drawIcecreamBar() {
    let lines = 4
    for _ in 1...lines {
        print("    | |")
    }
}

drawIcecreamBody()
drawIcecreamBar()
